            Scanner in = new Scanner(System.in);
            int num = in.nextInt();
            int[] testArray = new int[num];
            boolean[] keyArray = new boolean[num];
            for(int i  = 0;i<num;i++)
            {
                testArray[i] = in.nextInt();
                //???????????????????????????????????????????????????
                keyArray[i] = true;
            }
            //?????????????????????????????????????
            Arrays.sort(testArray);
            //?????��?
            isKey(testArray, keyArray);
            System.out.println(printKey(testArray, keyArray));
