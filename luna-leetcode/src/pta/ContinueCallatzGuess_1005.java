        //?��????
        static void isKey(int[] testArray,boolean[] keyArray)
        {
            //????????????
            for(int i = 0 ;i<testArray.length;i++)
            {
                int n = testArray[i];
                //??????????
                while(n!=1)
                {
                    if(n%2 == 0)
                    {
                        n = n/2;
                    }
                    else
                    {
                        n = (3*n+1)/2;
                    }
                    //???????????????????????????????
                    int pos = Arrays.binarySearch(testArray, n);
                    //?????????????????
                    if(pos>-1)
                    {
                        keyArray[pos] = false;
                    }
                }
            }
        }
        //????????????????????
        static String printKey(int[] testArray,boolean[] keyArray)
        {
            String res = "";
            //??????????�?????????????????????????????????
            for(int i = keyArray.length - 1;i>=0;i--)
            {
                if(keyArray[i])
                {
                    res += testArray[i] + " ";
                }
            }
            //????��?????
            res = res.trim();
            return res;
        }

